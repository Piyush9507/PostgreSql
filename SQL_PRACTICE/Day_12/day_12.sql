SELECT
	PRODUCT_ID,
	PRODUCT_NAME,
	(
		SELECT
			ROUND(AVG(PRICE), 0)
		FROM
			PRODUCTS.PRODUCTS
	) AS AVG_PRICE
FROM
	PRODUCTS.PRODUCTS
WHERE
	PRICE > (
		SELECT
			ROUND(AVG(PRICE), 0)
		FROM
			PRODUCTS.PRODUCTS
	)

