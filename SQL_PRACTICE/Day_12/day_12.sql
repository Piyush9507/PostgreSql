SELECT
	TICKET_ID,
	CATEGORY,
	EXTRACT(
		HOURS
		FROM
			(RESOLVED_DATE - CREATED_DATE)
	) AS RESOLUTION_HOURS
FROM
	SUPPORT.TICKETS
WHERE
	EXTRACT(
		HOURS
		FROM
			(RESOLVED_DATE - CREATED_DATE)
	) > (
		SELECT
			AVG(
				EXTRACT(
					HOURS
					FROM
						(RESOLVED_DATE - CREATED_DATE)
				)
			)
		FROM
			SUPPORT.TICKETS
		WHERE
			RESOLVED_DATE IS NOT NULL
	);

