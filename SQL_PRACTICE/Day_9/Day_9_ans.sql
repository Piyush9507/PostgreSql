SELECT
	C.FIRST_NAME,
	COUNT(*) AS TOTAL_ORDERS,
	COALESCE(SUM(O.NET_TOTAL),0) AS TOTAL_SPEND,
	COALESCE(ROUND(AVG(O.NET_TOTAL),2),0) AS AVG_ORDER_VALUE
FROM
	CUSTOMERS.CUSTOMERS C
	LEFT JOIN SALES.ORDERS O ON C.CUSTOMER_ID = O.CUST_ID
GROUP BY
	C.FIRST_NAME
ORDER BY TOTAL_SPEND;

