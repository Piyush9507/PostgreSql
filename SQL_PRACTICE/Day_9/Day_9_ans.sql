SELECT
	O.ORDER_ID,
	COUNT(R.RETURN_ID) AS LATE_RETURNS
FROM
	SALES.ORDERS O
	LEFT JOIN SALES.RETURNS R ON O.ORDER_ID = R.ORDER_ID
	AND R.RETURN_DATE > O.ORDER_DATE --NON-EQUI CONDITION
GROUP BY
	O.ORDER_ID;

-- ANSWER 18
SELECT
	C.CUSTOMER_ID,
	C.FIRST_NAME || ' ' || C.LAST_NAME AS CUSTOMER_NAME,
	COUNT(P.PAYMENT_ID) AS FAST_PAYMENTS_7DAYS
FROM
	CUSTOMERS.CUSTOMERS C
	LEFT JOIN SALES.ORDERS O ON C.CUSTOMER_ID = O.CUST_ID
	LEFT JOIN SALES.PAYMENTS P ON P.ORDER_ID = O.ORDER_ID
	AND P.PAYMENT_DATE >= O.ORDER_DATE
	AND P.PAYMENT_DATE <= O.ORDER_DATE + INTERVAL '7 days'
GROUP BY
	C.CUSTOMER_ID,
	C.FIRST_NAME,
	C.LAST_NAME;

