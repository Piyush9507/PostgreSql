SELECT 
	D.DEPT_NAME,
	COUNT(E.EMPLOYEE_ID) AS EMP_COUNT,
	COALESCE(ROUND(AVG(E.SALARY),2),0) AS AVG_SALARY,
	COALESCE(SUM(E.SALARY),0) AS TOTAL_SALARY
FROM
	CORE.DIM_DEPARTMENT D
	LEFT JOIN STORES.EMPLOYEES E USING(DEPT_ID)
GROUP BY
	D.DEPT_NAME
ORDER BY TOTAL_SALARY;

-- ANSWER 25
SELECT 
	P.PRODUCT_NAME,
	P.BRAND_ID,
	COALESCE(SUM(OI.QUANTITY),0) AS TOTAL_QTY,
	COALESCE(SUM(OI.NET_AMOUNT),0) AS TOTAL_REVENUE
FROM
	PRODUCTS.PRODUCTS P
	LEFT JOIN SALES.ORDER_ITEMS OI ON P.PRODUCT_ID = OI.PROD_ID
	WHERE OI.PROD_ID IS NOT NULL
GROUP BY
	P.PRODUCT_NAME,
	P.BRAND_ID
ORDER BY TOTAL_REVENUE;

